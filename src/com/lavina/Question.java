package com.lavina;

public class Question {
    private int qid;
    private String question, op1,op2, op3, op4, category,  ans,opted;
   private int weight;
    
//DC
    public Question() {
        opted="";
    }
    //PC

    public String getOpted() {
        return (opted!=null)?opted:"";
    }

    public void setOpted(String opted) {
        this.opted = opted;
    }

    public Question(int qid, String question, String op1, String op2, String op3, String op4, String category, int weight, String ans,String opted) {
        this.qid = qid;
        this.question = question;
        this.op1 = op1;
        this.op2 = op2;
        this.op3 = op3;
        this.op4 = op4;
        this.category = category;
        this.weight = weight;
        this.ans = ans;
        this.opted=opted;
    }

    public int getQid() {
        return qid;
    }

    public void setQid(int qid) {
        this.qid = qid;
    }

    public String getQuestion() {
        return question;
    }

    public void setQuestion(String question) {
        this.question = question;
    }

    public String getOp1() {
        return op1;
    }

    public void setOp1(String op1) {
        this.op1 = op1;
    }

    public String getOp2() {
        return op2;
    }

    public void setOp2(String op2) {
        this.op2 = op2;
    }

    public String getOp3() {
        return op3;
    }

    public void setOp3(String op3) {
        this.op3 = op3;
    }

    public String getOp4() {
        return op4;
    }

    public void setOp4(String op4) {
        this.op4 = op4;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public String getAns() {
        return ans;
    }

    public void setAns(String ans) {
        this.ans = ans;
    }

    @Override
    public String toString() {
     return "qid=" + qid;      
    }
    
    
}
